export function RolePortal({ role, onLogout }: { role: Role; onLogout: () => void }) {
  const [module, setModule] = useState("Home");
  const [showNotifications, setShowNotifications] = useState(false);
  const [showChatbot, setShowChatbot] = useState(false);
  const [chatPosition, setChatPosition] = useState({ x: window.innerWidth - 380, y: 120 });
  const [dragStart, setDragStart] = useState({ x: 0, y: 0 });
  const [isDragging, setIsDragging] = useState(false);
  const [messages, setMessages] = useState([
    { sender: "ai", text: `Hello! I am your Xebia AI Assistant. How can I help you with your ${role === "student" ? "Student" : role === "trainer" ? "Trainer" : "Admin"} dashboard today?` }
  ]);
  const [chatInput, setChatInput] = useState("");

  const handleMouseDown = (e: React.MouseEvent) => {
    const target = e.target as HTMLElement;
    if (target.closest('.chatbot-drag-handle')) {
      setIsDragging(true);
      setDragStart({
        x: e.clientX - chatPosition.x,
        y: e.clientY - chatPosition.y
      });
      e.preventDefault();
    }
  };

  useEffect(() => {
    const handleMouseMove = (e: MouseEvent) => {
      if (!isDragging) return;
      const newX = e.clientX - dragStart.x;
      const newY = e.clientY - dragStart.y;
      const maxX = window.innerWidth - 360;
      const maxY = window.innerHeight - 480;
      setChatPosition({
        x: Math.max(20, Math.min(maxX, newX)),
        y: Math.max(20, Math.min(maxY, newY))
      });
    };

    const handleMouseUp = () => {
      setIsDragging(false);
    };

    if (isDragging) {
      window.addEventListener('mousemove', handleMouseMove);
      window.addEventListener('mouseup', handleMouseUp);
    }
    return () => {
      window.removeEventListener('mousemove', handleMouseMove);
      window.removeEventListener('mouseup', handleMouseUp);
    };
  }, [isDragging, dragStart]);

  const handleSendMessage = () => {
    if (!chatInput.trim()) return;
    const userMsg = chatInput;
    setMessages(prev => [...prev, { sender: "user", text: userMsg }]);
    setChatInput("");

    setTimeout(() => {
      let aiText = "I've analyzed the requests. Let me know if you would like me to compile a summary of operations or update a widget context.";
      if (userMsg.toLowerCase().includes("timetable") || userMsg.toLowerCase().includes("schedule")) {
        aiText = role === "trainer"
          ? "Your next lecture is scheduled at 11:30 AM in Seminar Hall A. 48 learners are registered."
          : "Your next class is AI Systems at 09:00 AM in Lab 4.";
      } else if (userMsg.toLowerCase().includes("exam") || userMsg.toLowerCase().includes("grading")) {
        aiText = role === "trainer"
          ? "You have 12 assignments pending grading for the AI Foundations lab. Recommend finishing them before Friday."
          : "Your upcoming exam for AI Foundations is scheduled for this Friday at 10:00 AM. Study guide is available.";
      } else if (userMsg.toLowerCase().includes("hi") || userMsg.toLowerCase().includes("hello")) {
        aiText = `Hi there! I am your Xebia AI Assistant. What can I do for you today?`;
      }
      setMessages(prev => [...prev, { sender: "ai", text: aiText }]);
    }, 1000);
  };
  const modules = getRoleModules(role);
  const roleName = role === "student" ? "Student" : role === "trainer" ? "Trainer" : "Admin";
  const greeting = role === "student" ? "Good morning, Meera." : role === "trainer" ? "Good morning, Dr. Kavya." : "Institution command center";
  const searchPlaceholder = role === "student"
    ? "Search timetable, books, fees, faculty..."
    : role === "trainer"
      ? "Search courses, submissions, sessions, learners..."
      : "Search users, finance, reports, audit logs...";

  useEffect(() => {
    setModule("Home");
  }, [role]);

  const profile = getRoleUserProfile(role);
  const filteredModules = modules.filter(item => item !== "Settings" && item !== "Profile");

  return (
    <main className="student-os">
      <aside className="student-sidebar glass-card">
        <a className="brand" href="#top">
          <img className="brand-logo" src={xebiaLogo} alt="" />
          <span>Xebia {roleName}</span>
        </a>
        <nav aria-label={`${roleName} navigation`}>
          {filteredModules.map((item) => {
            const IconComponent = getModuleIcon(item);
            return (
              <button
                className={module === item ? "active" : ""}
                onClick={() => setModule(item)}
                key={item}
              >
                <span className="nav-icon-wrapper">
                  <IconComponent size={18} />
                </span>
                <span>{item}</span>
              </button>
            );
          })}
        </nav>

        <div className="sidebar-footer">
          <div className="sidebar-profile" style={{ cursor: "pointer" }} onClick={() => setModule("Profile")}>
            <div className="sidebar-profile-avatar">
              {profile.avatar}
            </div>
            <div className="sidebar-profile-text">
              <span className="profile-name">{profile.name}</span>
              <span className="profile-info">{profile.info}</span>
            </div>
          </div>
          <div className="sidebar-actions-card glass-card">
            <button
              className={`sidebar-action-btn ${module === "Settings" ? "active" : ""}`}
              onClick={() => setModule("Settings")}
            >
              <Settings size={18} />
              <span>Settings</span>
            </button>
            <button className="sidebar-action-btn logout-btn" onClick={onLogout}>
              <LogOut size={18} />
              <span>Logout</span>
            </button>
          </div>
        </div>
      </aside>

      <section className="student-workspace">
        <header className="student-topbar glass-card">
          <div className="topbar-left">
            <label className="portal-search">
              <Search size={18} />
              <input placeholder={searchPlaceholder} />
            </label>
          </div>
          <div className="topbar-right">
            <button
              className={`topbar-btn ai-assistant-btn ${showChatbot ? 'active' : ''}`}
              title="AI assistant"
              onClick={() => setShowChatbot(!showChatbot)}
            >
              <BrainCircuit size={18} />
              <span>AI Assistant</span>
            </button>
            <div style={{ position: "relative" }}>
              <button
                className="topbar-btn notification-bell-btn"
                title="Notifications"
                onClick={() => setShowNotifications(!showNotifications)}
              >
                <Bell size={18} />
                <span className="bell-badge"></span>
              </button>
              {showNotifications && (
                <div className="header-notifications-dropdown glass-card">
                  <div className="dropdown-header">
                    <h3>Notifications</h3>
                    <button className="mark-read-btn" onClick={() => setShowNotifications(false)}>Close</button>
                  </div>
                  <div className="dropdown-body">
                    <NotificationGroups role={role} isDropdown={true} />
                  </div>
                </div>
              )}
            </div>
            <div className="topbar-profile-avatar" onClick={() => setModule("Profile")}>
              {profile.avatar}
            </div>
          </div>
        </header>

        <div className="workspace-header">
          <nav className="portal-breadcrumb" aria-label="Breadcrumb">
            <span>Xebia LMS</span>
            <ChevronRight size={12} />
            <span>{roleName}</span>
            <ChevronRight size={12} />
            <strong className="current-crumb">{module}</strong>
          </nav>
          {module !== "Home" && (
            <h1 className="module-title">{module}</h1>
          )}
        </div>

        <AnimatePresence mode="wait">
          <motion.div
            key={module}
            initial={{ opacity: 0, y: 15 }}
            animate={{ opacity: 1, y: 0 }}
            exit={{ opacity: 0, y: -15 }}
            transition={{ duration: 0.3, ease: "easeOut" }}
            className="portal-content-wrapper"
          >
            <PortalContent role={role} module={module} onOpenChat={() => setShowChatbot(true)} />
          </motion.div>
        </AnimatePresence>
      </section>

      <nav className="student-bottom glass-card" aria-label={`Mobile ${roleName} navigation`}>
        {modules.slice(0, 5).map((item) => {
          const IconComponent = getModuleIcon(item);
          return (
            <button
              className={module === item ? "active" : ""}
              onClick={() => setModule(item)}
              key={item}
            >
              <IconComponent size={18} />
              <span>{item}</span>
            </button>
          );
        })}
      </nav>

      {showChatbot && (
        <div
          className="draggable-chatbot-window glass-card"
          style={{ left: chatPosition.x, top: chatPosition.y, position: 'fixed' }}
          onMouseDown={handleMouseDown}
        >
          <div className="chatbot-drag-handle">
            <div className="drag-title">
              <BrainCircuit size={16} />
              <span>AI Assistant</span>
            </div>
            <button className="chatbot-close-btn" onClick={() => setShowChatbot(false)}>
              <Plus size={16} style={{ transform: "rotate(45deg)" }} />
            </button>
          </div>
          <div className="chatbot-messages-area">
            {messages.map((msg, idx) => (
              <div key={idx} className={`chat-bubble-wrapper ${msg.sender}`}>
                <div className="chat-bubble">
                  {msg.text}
                </div>
              </div>
            ))}
          </div>
          <div className="chatbot-input-area">
            <input
              placeholder="Ask anything..."
              value={chatInput}
              onChange={e => setChatInput(e.target.value)}
              onKeyDown={e => { if (e.key === "Enter") handleSendMessage(); }}
            />
            <button onClick={handleSendMessage}>Send</button>
          </div>
        </div>
      )}
    </main>
  );
}

function getRoleModules(role: Role) {
  if (role === "trainer") return ["Home", "Courses", "Content", "Attendance", "Assignments", "Analytics", "Live Sessions", "Communication", "Profile", "Settings"];
  if (role === "admin") return ["Home", "Users", "Roles", "Curriculum", "Trainers", "Students", "Finance", "Reports", "Analytics", "Audit Logs"];
  return ["Home", "My Courses", "My Batches", "Attendance", "Assessments", "Results", "Notifications", "Feedback", "Profile", "Settings"];
}

function PortalContent({ role, module, onOpenChat }: { role: Role; module: string; onOpenChat: () => void }) {
  if (role === "trainer") return <TrainerContent module={module} onOpenChat={onOpenChat} />;
  if (role === "admin") return <AdminContent module={module} onOpenChat={onOpenChat} />;
  if (module === "Home") return <StudentHome onOpenChat={onOpenChat} />;
  if (module === "My Courses") return <MyCoursesModule />;
  if (module === "Attendance") return <StudentAttendanceModule />;
  if (module === "My Batches") return <MyBatchesModule />;
  if (module === "Assessments") return <AssessmentsModule />;
  if (module === "Results") return <ResultsModule />;
  if (module === "Notifications") return <NotificationsModule />;
  if (module === "Feedback") return <FeedbackModule />;
  if (module === "Profile") return <StudentProfileModule />;
  return <StudentSettingsModule />;
}

